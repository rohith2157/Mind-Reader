# Build the full base list with categories
all_entities = []
for name, feats in animals:
    all_entities.append((name, 'animal', feats))
for name, feats in humans:
    all_entities.append((name, 'human', feats))
for name, feats in objects:
    all_entities.append((name, 'object', feats))
for name, feats in plants:
    all_entities.append((name, 'plant', feats))
for name, feats in fictional:
    all_entities.append((name, 'fictional', feats))

# ── Ensure every entity has a UNIQUE feature vector ─────────────────────
seen_vectors = {}
for i, (name, cat, feats) in enumerate(all_entities):
    key = tuple(feats[c] for c in FEATURE_COLS)
    if key in seen_vectors:
        # Flip features one by one until unique
        new_feats = dict(feats)
        for col in FEATURE_COLS:
            new_feats[col] = 1 - new_feats[col]
            new_key = tuple(new_feats[c] for c in FEATURE_COLS)
            if new_key not in seen_vectors:
                all_entities[i] = (name, cat, new_feats)
                seen_vectors[new_key] = name
                break
            new_feats[col] = feats[col]  # revert and try next
    else:
        seen_vectors[key] = name

print(f"Total unique entities: {len(all_entities)}")
print(f"Unique feature vectors: {len(seen_vectors)}")

# ── Build 1000-row dataset (NO noise — exact copies) ────────────────────
rows = []

# First pass: add all unique entities once
for name, category, feats in all_entities:
    row = {'name': name, **feats, 'category': category}
    rows.append(row)

# Second pass: repeat entities exactly to reach 1000 rows
target_rows = 1000
while len(rows) < target_rows:
    name, category, feats = random.choice(all_entities)
    row = {'name': name, **feats, 'category': category}
    rows.append(row)

# Create DataFrame
df = pd.DataFrame(rows)

# Shuffle the dataset
df = df.sample(frac=1, random_state=42).reset_index(drop=True)

